class Calculadora {

    public String estado;
    public String resultado;

    public int suma(int n1, int n2) {

        return n1 + n2;
    }

    public int resta(int n1, int n2) {

        return n1 - n2;

    }

    public int multi(int n1, int n2) {

        return n1 * n2;

    }

    public double division(double n1, double n2) {

        return n1 / n2;
    }

}
